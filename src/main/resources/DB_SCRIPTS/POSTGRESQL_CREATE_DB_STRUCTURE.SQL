CREATE TABLE MED_USERS (
  USER_ID     INTEGER NOT NULL PRIMARY KEY,
  FIRST_NAME  CHAR(25),
  LAST_NAME   CHAR(25),
  MIDDLE_NAME CHAR(25),
  USER_TYPE   CHAR(20) NOT NULL,
  EMAIL       CHAR(50),
  PASSWORD    CHAR(50) NOT NULL
);

CREATE TABLE MED_PATIENTS (
  PATIENT_ID  INTEGER NOT NULL PRIMARY KEY,
  FIRST_NAME  CHAR(25),
  LAST_NAME   CHAR(25),
  MIDDLE_NAME CHAR(25),
  BIRTHDAY   DATE,
  SEX         CHAR(10),
  DOCTOR_ID INTEGER NOT NULL REFERENCES MED_USERS(USER_ID),
  DESCRIPTION CHAR(4000)
);

CREATE TABLE MED_TESTS (
  TEST_ID INTEGER NOT NULL PRIMARY KEY,
  PATIENT_ID INTEGER NOT NULL,
  TEMPLATE_ID INTEGER NOT NULL,
  DATE DATE --,
  --DEVICE_ID
);

CREATE TABLE MED_TEST_POINTS (
  POINT_ID INTEGER NOT NULL PRIMARY KEY,
  TEST_ID INTEGER NOT NULL,
  FREQUENCY INTEGER,
  --INTENSITY_VALUE_ID
  INTENSITY_VALUE INTEGER,
  TEMPLATE_POINT_ID INTEGER
);
